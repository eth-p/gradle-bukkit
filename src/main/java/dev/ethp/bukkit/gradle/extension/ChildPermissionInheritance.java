package dev.ethp.bukkit.gradle.extension;

public enum ChildPermissionInheritance {


	// -------------------------------------------------------------------------------------------------------------
	// Values
	// -------------------------------------------------------------------------------------------------------------

	INHERIT("true"),
	INVERT("false");

	// -------------------------------------------------------------------------------------------------------------
	// Fields
	// -------------------------------------------------------------------------------------------------------------

	private String string;

	// -------------------------------------------------------------------------------------------------------------
	// Constructors
	// -------------------------------------------------------------------------------------------------------------

	ChildPermissionInheritance(String string) {
		this.string = string;
	}

	// -------------------------------------------------------------------------------------------------------------
	// Getters
	// -------------------------------------------------------------------------------------------------------------

	/**
	 * Gets the inheritance value string.
	 * This is used in the plugin manifest.
	 *
	 * @return The inheritance value string.
	 */
	public String getString() {
		return this.string;
	}

}
